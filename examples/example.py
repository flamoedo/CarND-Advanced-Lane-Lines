def warp(img):
    src = np.float32(
            [[680, 448],
            [924, 605],
            [372, 605],
            [600, 448]])
        
    dst = np.float32([[900, 0],
                      [900, 700],
                      [350, 700],
                      [350, 0]])
    
    
    M = cv2.getPerspectiveTransform(src, dst)
    
    img_size = (img.shape[1], img.shape[0])
    
    return cv2.warpPerspective(img, M, img_size, flags=cv2.INTER_LINEAR)



